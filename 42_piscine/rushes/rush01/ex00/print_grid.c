#include <unistd.h>

void	ft_putnbr(int nb);

void	print_grid(int grid[4][4])
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (j < 4)
	{
		i = 0;
		while (i < 4)
		{
			ft_putnbr(grid[j][i]);
			if (i != 3)
				write(1, " ", 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
